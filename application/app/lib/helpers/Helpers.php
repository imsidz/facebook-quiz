function arrayGetValByNestedKeys(array $array, $keys){
    $key = array_shift($keys);
    if(!isset($array[$key])) return null;
    return empty($keys) ?
        $array[$key]:
        arrayGetValByNestedKeys($array[$key], $keys);
}


    public static function isValidUrl($url) {
        $url = Helpers::urlWithHttp($url);
        if(filter_var($url, FILTER_VALIDATE_URL) === FALSE)
            return false;

        //filter_var allows single words like "hbo" to be validated as url. so check that specifically.
        if(!strpos($url, '.')) {
            return false;
        }
        return true;
    }

    public static function urlWithHttp($url) {
        if (!preg_match("~^(?:f|ht)tps?://~i", $url)) {
            $url = "http://" . $url;
        }
        return $url;
    }

    public static function getDomainFromUrl($url) {
        try {
            $parse = parse_url($url);
            return $parse['host'];
        } catch(Exception $e) {
            return '';
        }
    }


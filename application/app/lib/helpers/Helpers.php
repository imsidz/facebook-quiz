    public static function getLanguageFromDomain() {
        $domain = parse_url(Request::root())['host'];
        if($domain == BASE_DOMAIN) {
            //Default domian - not language specific
            return null;
        }
        $domainParts = explode('.', $domain);
        return $domainParts[0];
    }

    public static function getBaseDomain()
    {
        $host = parse_url(Request::root())['host'];
        $hostParts = explode('.', $host);
        if(strtolower($hostParts[0]) == "www")
            array_shift($hostParts);
        if(count($hostParts) > 1 && strlen($hostParts[0]) <= 3) {
            array_shift($hostParts);
        }
        return implode('.', $hostParts);
    }

    public static function getLanguageHomeUrl($languageCode)
    {
        $homeUrl = route('home');
        $parsedUrl = parse_url($homeUrl);
        $parsedUrl['host'] = $languageCode . '.' . BASE_DOMAIN;
        $parsedUrl['path'] = !empty($parsedUrl['path']) ? $parsedUrl['path'] : '';
        $languageHomeUrl = \Request::getScheme() . '://' . "{$parsedUrl['host']}{$parsedUrl['path']}";
        return $languageHomeUrl;
    }

